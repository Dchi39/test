# Program to calculate the sum and average of two numbers

# Input: Two numbers
x = float(input("Enter the first number (x): "))
y = float(input("Enter the second number (y): "))

# Calculate sum
sum_result = x + y

# Calculate average
average_result = sum_result / 2

# Output the results
print(f"Sum: {sum_result}")
print(f"Average: {average_result}")

